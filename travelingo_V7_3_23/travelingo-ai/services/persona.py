"""
페르소나 관리 및 시스템 프롬프트 생성 모듈
각 챕터별 페르소나와 사용자 레벨에 따른 난이도 조절 로직 포함
"""

# ============================================
# 기본 페르소나 사전
# ============================================

DEFAULT_PERSONAS = {
    1: "인천공항 체크인 카운터 직원",
    2: "공항 입국 심사관",
    3: "택시/우버 기사",
    4: "호텔 프론트 데스크 직원",
    5: "레스토랑 웨이터/웨이트리스",
    6: "레스토랑 소믈리에/바리스타",
    7: "관광 안내소 직원",
    8: "쇼핑몰 판매원",
    9: "병원 응급실 접수 직원",
    10: "공항 면세점 직원"
}


def get_persona_description(chapter_id: int) -> str:
    """
    챕터 ID에 해당하는 기본 페르소나 설명을 반환하는 함수

    Args:
        chapter_id: 챕터 번호 (1-10)

    Returns:
        str: 해당 챕터의 페르소나 설명
            유효하지 않은 ID일 경우 기본 페르소나 반환
    """

    # 챕터 ID가 유효한 범위 내에 있는지 확인
    if chapter_id in DEFAULT_PERSONAS:
        return DEFAULT_PERSONAS[chapter_id]
    else:
        # 유효하지 않은 ID일 경우 기본 페르소나 반환
        return DEFAULT_PERSONAS[1]


def get_system_prompt(persona: str, user_level: int) -> str:
    """
    페르소나와 사용자 레벨을 기반으로 상세한 시스템 프롬프트를 생성하는 함수

    이 함수는 OpenAI GPT에 다음을 지시함:
    1. 주어진 페르소나로 역할극을 수행
    2. 사용자 레벨에 따라 난이도 조절
    3. 항상 지정된 JSON 형식으로 응답
    4. 레벨별 피드백 스타일 조정

    Args:
        persona: 역할극할 페르소나 설명 (예: "호텔 프론트 데스크 직원")
        user_level: 사용자 레벨
            - 1: 초급 (Beginner) - 간단한 어휘, 상세한 한국어 피드백
            - 2: 중급 (Intermediate) - 일반적인 난이도
            - 3: 고급 (Advanced) - 복잡한 표현, 최소한의 한국어

    Returns:
        str: 레벨에 맞춘 상세한 시스템 프롬프트
    """

    # ========================================
    # 레벨별 프롬프트 매개변수 정의
    # ========================================

    if user_level == 1:
        # 초급 레벨 설정
        level_instruction = """
        당신은 영어 학습자가 이해할 수 있도록 최대한 간단한 어휘와 문법을 사용해야 합니다.
        - 짧고 간단한 문장 사용
        - 한 문장은 10단어 이내로 제한
        - 복잡한 시제는 피하고 현재형 중심
        - 일반적인 단어만 사용

        피드백은 상세하고 친절한 한국어로 작성:
        - 사용자의 실수를 구체적으로 설명
        - 올바른 표현과 틀린 표현 비교
        - 문법 규칙을 한국어로 설명
        - 격려와 칭찬 포함
        """
        feedback_style = "상세하고 친절한 한국어 피드백"

    elif user_level == 3:
        # 고급 레벨 설정
        level_instruction = """
        당신은 영어 고급 학습자와 대화하고 있다고 가정합니다.
        - 자연스러운 관용구와 표현 사용
        - 복잡한 문법 구조 사용 가능
        - 뉘앙스 있는 표현 사용
        - 실제 원어민처럼 자연스럽게 대화

        피드백은 간결한 한국어로 작성:
        - 주요 오류만 지적
        - 더 자연스러운 표현 제안
        - 최소한의 한국어 설명
        - 칭찬은 간결하게
        """
        feedback_style = "간결하고 최소화된 한국어 피드백"

    else:
        # 중급 레벨 설정 (기본값)
        level_instruction = """
        당신은 중급 영어 학습자와 대화하고 있다고 가정합니다.
        - 일반적인 난이도의 어휘와 표현 사용
        - 자연스러운 문법 구조 사용
        - 학습에 도움이 되는 표현 선호

        피드백은 균형잡힌 한국어로 작성:
        - 주요 오류와 개선점 지적
        - 칭찬할 점은 인정
        - 설명은 이해하기 쉽게
        """
        feedback_style = "균형잡힌 한국어 피드백"

    # ========================================
    # 최종 시스템 프롬프트 생성
    # ========================================

    system_prompt = f"""당신은 '{persona}'로 역할극을 해야 합니다.
상황에 맞게 자연스럽고 현실적인 대화를 진행하세요.

{level_instruction}

사용자의 각 메시지에 대해 다음 정확한 형식의 JSON으로 응답해야 합니다:
{{
    "reply": "사용자에게 하는 영어 응답 (역할극 유지)",
    "feedback": "{feedback_style}",
    "betterExpression": "사용자가 한 말을 더 자연스럽고 적절하게 표현하는 방법 (영어)",
    "translation": "당신의 영어 응답을 한국어로 번역한 것"
}}

중요 지침:
1. 역할극을 일관되게 유지하세요
2. 항상 위의 JSON 형식으로만 응답하세요 (마크다운 코드 블록 없이)
3. 피드백은 건설적이고 격려적이어야 합니다
4. betterExpression은 사용자의 실수를 바로잡는 것입니다
5. translation은 당신의 답변을 한국어로 번역하는 것입니다 (사용자의 말이 아닙니다)
6. JSON은 유효한 형식이어야 합니다 (특수 문자는 이스케이프 처리)

이제 대화를 시작하세요."""

    return system_prompt
